#include <iostream>
using namespace std ; 

int main()
{
	int a = 8 , b = 4 ;
	cout << "a = 8 | b = 4" << endl ;
	cout << endl ;
	cout << "Addition result: " << ( a + b ) << endl ;
	cout << "Subtraction result: " << ( a - b ) << endl ;
	cout << "Multiplication result: " << ( a * b ) << endl ;
	cout << "Division result: " << ( a / b ) << endl ;
	cout << "Modulus result: " << ( a % b ) << endl ;
	
	return 0 ;
}